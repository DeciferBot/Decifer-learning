/**
 * Periodic Table explorer seed — 118 elements.
 *
 * Source: scripts/data/ptable.json (Bowserinator/Periodic-Table-JSON; element
 * summaries derived from Wikipedia, CC BY-SA 3.0). Transforms into the
 * explorers/explorer_nodes schema (scene_type 'periodic-table').
 *
 * Default emits idempotent SQL (no local service key needed):
 *   node scripts/seed-periodic-table.mjs --sql > /tmp/pt.sql
 *   npx prisma db execute --url "$DATABASE_URL" --file /tmp/pt.sql
 * With a service key: node --env-file=.env.local scripts/seed-periodic-table.mjs
 */

import { readFileSync } from 'node:fs'
import { fileURLToPath } from 'node:url'
import { dirname, join } from 'node:path'

const args = process.argv.slice(2)
const SQL_MODE = args.includes('--sql')
const PUBLISH = !args.includes('--unpublish')

const __dir = dirname(fileURLToPath(import.meta.url))
const raw = JSON.parse(readFileSync(join(__dir, 'data', 'ptable.json'), 'utf8'))
const ELEMENTS = raw.elements.filter((e) => e.number >= 1 && e.number <= 118)

const CATEGORIES = [
  { key: 'alkali-metal',          label: 'Alkali metal',          color: '#FF6B6B' },
  { key: 'alkaline-earth-metal',  label: 'Alkaline earth metal',  color: '#FFA94D' },
  { key: 'transition-metal',      label: 'Transition metal',      color: '#FFD43B' },
  { key: 'post-transition-metal', label: 'Post-transition metal', color: '#74C0FC' },
  { key: 'metalloid',             label: 'Metalloid',             color: '#63E6BE' },
  { key: 'diatomic-nonmetal',     label: 'Reactive nonmetal',     color: '#52D9A0' },
  { key: 'polyatomic-nonmetal',   label: 'Reactive nonmetal',     color: '#52D9A0' },
  { key: 'noble-gas',             label: 'Noble gas',             color: '#A78BFA' },
  { key: 'lanthanide',            label: 'Lanthanide',            color: '#F783AC' },
  { key: 'actinide',              label: 'Actinide',              color: '#E599F7' },
  { key: 'unknown',               label: 'Unknown / predicted',   color: '#868E96' },
]
const COLOR_BY_KEY = Object.fromEntries(CATEGORIES.map((c) => [c.key, c.color]))

function categoryKey(category) {
  const c = (category || '').toLowerCase().trim()
  if (c.startsWith('unknown')) return 'unknown'
  const slug = c.replace(/\s+/g, '-')
  return COLOR_BY_KEY[slug] ? slug : 'unknown'
}

function buildFacts(e) {
  const facts = []
  if (e.appearance) facts.push(`Appearance: ${e.appearance}`)
  facts.push(`State at room temperature: ${e.phase ?? 'unknown'}`)
  if (e.discovered_by) facts.push(`Discovered by ${e.discovered_by}`)
  if (e.named_by) facts.push(`Named by ${e.named_by}`)
  facts.push(`Period ${e.period}${e.group ? `, Group ${e.group}` : ''} · ${e.block}-block`)
  return facts
}

const EXPLORER = {
  key: 'periodic-table',
  title: 'Periodic Table',
  tagline: 'Tap elements, discover chemistry, see how matter is built',
  emoji: '⚗️',
  scene_type: 'periodic-table',
  gradient: 'linear-gradient(135deg, #002147 0%, #003580 50%, #004a9f 100%)',
  order_index: 2,
  config: {
    background: '#0a0a18',
    categories: CATEGORIES.filter((c, i) => CATEGORIES.findIndex((x) => x.label === c.label) === i),
    attribution: 'Element data & summaries from Wikipedia (CC BY-SA 3.0) via Bowserinator/Periodic-Table-JSON.',
    completion: { title: 'Element Explorer!', body: 'You started building your understanding of matter.', points: 100 },
  },
}

const NODES = ELEMENTS.map((e) => {
  const key = categoryKey(e.category)
  return {
    key: e.symbol.toLowerCase(),
    name: e.name,
    order_index: e.number,
    visual: {
      symbol: e.symbol,
      number: e.number,
      xpos: e.xpos,
      ypos: e.ypos,
      group: e.group ?? null,
      period: e.period,
      categoryKey: key,
      color: COLOR_BY_KEY[key],
    },
    stats: {
      atomic_mass: Math.round((e.atomic_mass + Number.EPSILON) * 1000) / 1000,
      phase: e.phase ?? 'Unknown',
      category: e.category,
      block: e.block,
      discovered_by: e.discovered_by ?? null,
      named_by: e.named_by ?? null,
      appearance: e.appearance ?? null,
      electron_configuration_semantic: e.electron_configuration_semantic ?? null,
    },
    content: {
      summary: e.summary,
      narration: e.summary,
      facts: buildFacts(e),
    },
    quiz: null,
  }
})

const sqlStr = (v) => (v == null ? 'NULL' : `'${String(v).replace(/'/g, "''")}'`)
const sqlJson = (v) => (v == null ? 'NULL' : `'${JSON.stringify(v).replace(/'/g, "''")}'::jsonb`)

function emitSql() {
  const e = EXPLORER
  const lines = ['-- Generated by scripts/seed-periodic-table.mjs --sql. Idempotent.', 'BEGIN;']
  lines.push('INSERT INTO explorers (key, title, tagline, emoji, scene_type, gradient, config, order_index, is_published, updated_at)')
  lines.push(`VALUES (${sqlStr(e.key)}, ${sqlStr(e.title)}, ${sqlStr(e.tagline)}, ${sqlStr(e.emoji)}, ${sqlStr(e.scene_type)}, ${sqlStr(e.gradient)}, ${sqlJson(e.config)}, ${e.order_index}, ${PUBLISH}, now())`)
  lines.push('ON CONFLICT (key) DO UPDATE SET title=EXCLUDED.title, tagline=EXCLUDED.tagline, emoji=EXCLUDED.emoji, scene_type=EXCLUDED.scene_type, gradient=EXCLUDED.gradient, config=EXCLUDED.config, order_index=EXCLUDED.order_index, is_published=EXCLUDED.is_published, updated_at=now();')
  lines.push(`DELETE FROM explorer_nodes WHERE explorer_id = (SELECT id FROM explorers WHERE key = ${sqlStr(e.key)});`)
  for (const n of NODES) {
    lines.push('INSERT INTO explorer_nodes (explorer_id, key, name, order_index, visual, stats, content, quiz)')
    lines.push(`VALUES ((SELECT id FROM explorers WHERE key = ${sqlStr(e.key)}), ${sqlStr(n.key)}, ${sqlStr(n.name)}, ${n.order_index}, ${sqlJson(n.visual)}, ${sqlJson(n.stats)}, ${sqlJson(n.content)}, ${sqlJson(n.quiz)});`)
  }
  lines.push('COMMIT;')
  console.log(lines.join('\n'))
}

async function main() {
  if (SQL_MODE) { emitSql(); return }
  const { createClient } = await import('@supabase/supabase-js')
  const url = process.env.NEXT_PUBLIC_SUPABASE_URL, key = process.env.SUPABASE_SERVICE_ROLE_KEY
  if (!url || !key) { console.error('Missing env; use --sql to emit SQL instead.'); process.exit(1) }
  const supabase = createClient(url, key, { auth: { persistSession: false } })
  const { data: ex, error } = await supabase.from('explorers').upsert({
    key: EXPLORER.key, title: EXPLORER.title, tagline: EXPLORER.tagline, emoji: EXPLORER.emoji,
    scene_type: EXPLORER.scene_type, gradient: EXPLORER.gradient, config: EXPLORER.config,
    order_index: EXPLORER.order_index, is_published: PUBLISH, updated_at: new Date().toISOString(),
  }, { onConflict: 'key' }).select('id').single()
  if (error) throw error
  await supabase.from('explorer_nodes').delete().eq('explorer_id', ex.id)
  const { error: insErr } = await supabase.from('explorer_nodes').insert(NODES.map((n) => ({ explorer_id: ex.id, ...n })))
  if (insErr) throw insErr
  console.log(`✓ Seeded periodic-table with ${NODES.length} elements.`)
}

main().catch((e) => { console.error('Seed failed:', e.message ?? e); process.exit(1) })
